export interface IUser {
    $key?: string,
    dateCreated: string
}

export class User {
    constructor(
        public $key: string,
        public dateCreated: string
    ) {}

    fromJson({$key, dateCreated}): User {
        return new User($key, dateCreated);
    }
}

